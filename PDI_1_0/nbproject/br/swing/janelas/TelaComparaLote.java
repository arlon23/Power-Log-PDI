    private void processa2ResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processa2ResultActionPerformed
        // TODO add your handling code here:
        JFrame frame= new JFrame("JOptionPane showMessageDialog exemple");
        
        try {
            // pega a lista de arquivos da pasta groundTruth
            if (!groundFolderTxt.getText().isEmpty() && !imagesFolderTxt.getText().isEmpty()
                    && !arquivoDestTxt.getText().isEmpty()) {
                File fileGround = new File(groundFolderTxt.getText());
                File[] pathsGround;
                // returns pathnames for files and directory
                pathsGround = fileGround.listFiles();

                File fileImages = new File(imagesFolderTxt.getText());
                File[] pathsImages;
                // returns pathnames for files and directory
                pathsImages = fileImages.listFiles();
                   
                FileWriter arq = new FileWriter(arquivoDestTxt.getText() + File.separator + "resultado.csv");
                PrintWriter gravarArq = new PrintWriter(arq);

                gravarArq.printf("ImgGT;ImgT;TP;TN;FP;FN;ME;RAE;Precision;Accuracy;Recall;Error;FMeasure;NRM\n");
                // for each pathname in pathname array                
                for (File pathG : pathsGround) {
                    
                    String gT = pathG.getName();
                    
                    for (File pathImg : pathsImages) {
                        
                        String img = pathImg.getName();
                        if (img.contains(gT.substring(0, gT.length() - 6))) {
                            
                            BufferedImage imgI = ImageIO.read(pathG);
                            BufferedImage imgT = ImageIO.read(pathImg);
                            
                            CompareLimiarizacao cp = new CompareLimiarizacao();
                            //JOptionPane.showMessageDialog(frame, "saiu!");
                            cp.comparar(imgI, imgT);                            

                            String config = "proc_" + pathImg.getName();
                            gravarArq.printf(pathG.getName() + ";" + config + ";" + cp.toStringArq() + ";\n");

                        }
                    }
                    System.out.println("Processando..." + pathG.getName());
                }
                arq.close();
            }//if 
            else {
                System.out.println("Algum dado est√° faltando.");
            }
            jLabel2.setVisible(true);
            System.out.println("PRONTO!");

        } catch (Exception e) {
            // if any error occurs
            e.printStackTrace();
        }
    }//GEN-LAST:event_processa2ResultActionPerformed

