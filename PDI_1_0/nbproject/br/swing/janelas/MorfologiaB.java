    private void processarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processarActionPerformed
        // TODO add your handling code here:

        String op = operacoes.getSelectedItem().toString();
        String ee = "";

        int numIteration = Integer.parseInt(numItera.getText());

        if (quadrado.isSelected()) {
            ee = "quadrado";
        } else if (horizontal.isSelected()) {
            ee = "horizontal";
        } else if (vertical.isSelected()) {
            ee = "vertical";
        } else if (rombus.isSelected()) {
            ee = "rombus";
        } else if (cruz.isSelected()) {
            ee = "cruz";
        }

        BufferedImage imagem = mainFrame2.getSelected().getImage();
        MorfologiaBinaria mb = new MorfologiaBinaria();
        for (int i = 0; i < numIteration; i++) {
            if (op == "Erosao") {
                imagem = mb.erosao(imagem, ee);
            } else if (op == "Dilatacao") {
                imagem = mb.dilatacao(imagem, ee);
            } else if (op == "Abertura") {
                imagem = mb.abertura(imagem, ee);
            } else if (op == "Fechamento") {
                imagem = mb.fechamento(imagem, ee);
            }
        }

        try {

            JImageWindow imgWindow = new JImageWindow(imagem, JImageWindow.Tipo.NORMAL);
            imgWindow.addInternalFrameListener(new InternalFrameAdapter() {
                public void internalFrameActivated(javax.swing.event.InternalFrameEvent e) {
                    mainFrame2.setSelected((JImageWindow) e.getSource());
                }

                ;

                    public void internalFrameClosed(javax.swing.event.InternalFrameEvent e) {
                    JImageWindow closed = (JImageWindow) e.getSource();
                    mainFrame2.getDesktopPane().remove(closed);
                }
            ;
            });

            imgWindow.setVisible(true);
            imgWindow.setLocation(
                    mainFrame2.getSelected().getLocation().x + 50,
                    mainFrame2.getSelected().getLocation().y + 50);

            mainFrame2.getDesktopPane().add(imgWindow);
            imgWindow.setSelected(true);
        } catch (Exception e1) {
            JOptionPane.showMessageDialog(mainFrame2, "Falha ao carregar a imagem");
            e1.printStackTrace();
        }

    }//GEN-LAST:event_processarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String op = operacoes.getSelectedItem().toString();
        String ee = "";

        int numIteration = Integer.parseInt(numItera.getText());

        if (quadrado.isSelected()) {
            ee = "quadrado";
        } else if (horizontal.isSelected()) {
            ee = "horizontal";
        } else if (vertical.isSelected()) {
            ee = "vertical";
        } else if (rombus.isSelected()) {
            ee = "rombus";
        } else if (cruz.isSelected()) {
            ee = "cruz";
        }

        ManipulaArquivo ma = new ManipulaArquivo();
        //PEGAR PASTA PARA SER PROCESSADA....
        File arqImgs[] = ma.abrePasta("Escolha a pasta das imagens a serem Segmentadas");
        //PEGAR PASTA PARA GUARDAR RESULTADO...
        String pastaSalvar = ma.selecionarDiretorio("Escolha a pasta onde serão guardadas as imagens processadas");
        pastaSalvar += File.separator;
        for (File path : arqImgs) {
            if (path.getName().contains(".jpg") || path.getName().contains(".bmp") || path.getName().contains(".png")) {
                try {

                    System.out.println("Processando: " + path.getName());
                    BufferedImage imagem = ImageIO.read(path);

                    if (!Paths.get(pastaSalvar).toFile().exists()) {
                        (new File(pastaSalvar)).mkdir();
                    }
                    MorfologiaBinaria mb = new MorfologiaBinaria();
                    for (int i = 0; i < numIteration; i++) {
                        if (op == "Erosao") {
                            imagem = mb.erosao(imagem, ee);
                        } else if (op == "Dilatacao") {
                            imagem = mb.dilatacao(imagem, ee);
                        } else if (op == "Abertura") {
                            imagem = mb.abertura(imagem, ee);
                        } else if (op == "Fechamento") {
                            imagem = mb.fechamento(imagem, ee);
                        }
                    }
                    File outputFile = new File(pastaSalvar + path.getName() + "_" + op + ".png");
                    ImageIO.write(imagem, "PNG", outputFile);

                    jLabel1.setVisible(true);
                } catch (Exception ex) {
                    System.out.println("Probelma ao processar metodo...." + ex.getMessage());
                }
            } else {
                System.out.println("Não é imagem...");
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

